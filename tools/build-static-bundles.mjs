import { readFile, writeFile } from "node:fs/promises";
import path from "node:path";
import { fileURLToPath } from "node:url";

const root = path.resolve(path.dirname(fileURLToPath(import.meta.url)), "..");

async function bundle(output, inputs, separator) {
  const contents = await Promise.all(
    inputs.map(async (input) => {
      const source = await readFile(path.join(root, input), "utf8");
      return `/* ${input} */${separator}${source.trim()}${separator}`;
    }),
  );
  await writeFile(
    path.join(root, output),
    `/* Generated by tools/build-static-bundles.mjs. */${separator}${contents.join(separator)}`,
    "utf8",
  );
}

await Promise.all([
  bundle(
    "lobby-app.bundle.js",
    ["supabase-config.js", "multiplayer.js", "lobby.js"],
    "\n\n",
  ),
  bundle(
    "future-app.bundle.js",
    ["supabase-config.js", "multiplayer.js", "future.js"],
    "\n\n",
  ),
  bundle(
    "future-app.bundle.css",
    ["lobby.css", "styles.css", "future.css"],
    "\n\n",
  ),
]);
