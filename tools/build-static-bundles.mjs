import { readFile, writeFile } from "node:fs/promises";
import path from "node:path";
import { fileURLToPath } from "node:url";

const root = path.resolve(path.dirname(fileURLToPath(import.meta.url)), "..");

async function bundle(output, inputs, separator) {
  const contents = await Promise.all(
    inputs.map(async (input) => {
      const source = await readFile(path.join(root, input), "utf8");
      return `/* ${input} */${separator}${source.trim()}${separator}`;
    }),
  );
  await writeFile(
    path.join(root, output),
    `/* Generated by tools/build-static-bundles.mjs. */${separator}${contents.join(separator)}`,
    "utf8",
  );
}

async function bundleBuilderData() {
  const inputs = {
    raceFeatures: "Races/features.json",
    subraces: "Races/subraces.json",
    classFeatures: "Classes/features.json",
    classOptions: "Classes/options.json",
    spells: "Spells/spells.json",
    originFeats: "Feats/origin-feats.json",
  };
  const data = Object.fromEntries(
    await Promise.all(
      Object.entries(inputs).map(async ([key, input]) => [
        key,
        JSON.parse(await readFile(path.join(root, input), "utf8")),
      ]),
    ),
  );
  await writeFile(
    path.join(root, "builder-data.bundle.js"),
    `/* Generated by tools/build-static-bundles.mjs. */\nwindow.AVTIZM_BUILDER_DATA = ${JSON.stringify(data)};\n`,
    "utf8",
  );
}

await Promise.all([
  bundle(
    "lobby-app.bundle.js",
    ["supabase-config.js", "multiplayer.js", "lobby.js"],
    "\n\n",
  ),
  bundle(
    "future-app.bundle.js",
    ["supabase-config.js", "multiplayer.js", "future.js"],
    "\n\n",
  ),
  bundle(
    "future-app.bundle.css",
    ["lobby.css", "styles.css", "future.css"],
    "\n\n",
  ),
  bundleBuilderData(),
]);
